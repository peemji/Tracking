        // Disable the auto zoom flag (disabled by default)
        $map->setAutoZoom(false);

        // Sets the center
        $map->setCenter(new Coordinate(0, 0));

        // Sets the zoom
        $map->setMapOption('zoom', 3);
        $map->setHtmlId('map_canvas');
        return view('map.map',compact('maps'));
